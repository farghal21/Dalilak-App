class CarModel {
  final String id;
  final String name;
  final String price;
  final String image;
  final String location;
  final String year;
  final String description;

  CarModel({
    required this.id,
    required this.name,
    required this.price,
    required this.image,
    required this.location,
    required this.year,
    required this.description,
  });
}
